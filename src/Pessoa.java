public class Pessoa {
    public String nome;
    public String sobrenome;
    public String CPF;
    public String RG;
    public double telefone;
    public String endereco;

    public Pessoa(String nome,String sobrenome, String CPF, String RG, double telefone, String endereco){
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.CPF = CPF;
        this.RG = RG;
        this.telefone = telefone;
        this.endereco = endereco;
    }

}
